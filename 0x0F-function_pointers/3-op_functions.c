#include "3-calc.h"
/**
 * op_add-calculates sum of two integers
 * @a:fist int
 * @b:sec int
 * Return:sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub-calculates the diff of two numbers
 * @a:first int
 * @b:sec int
 * Return: diff of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul-calculates the product of two numbers
 * @a:first int
 * @b:sec int
 * Return: product of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div-calculates the division of two numbers
 * @a:first int
 * @b:sec int
 * Return: quotient  of a by b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod-calculates the remainder of div of two numbers
 * @a:first int
 * @b:sec int
 * Return: remainder of a / b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
